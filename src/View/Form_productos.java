    public void cargartabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        List<Productos> med = cp.findProductosEntities();
        for (int i = 0; i < med.size(); i++) {
            Object fila[] = {med.get(i).getCodigo(), med.get(i).getNombre(), med.get(i).getCategoriasCodigo(), med.get(i).getProveedoresCodigo(), med.get(i).getDescripcion(), med.get(i).getUnidadMedida(), med.get(i).getPrecioUnitario(), med.get(i).getCantidadDisponible(), med.get(i).getCantidadIngreso(), med.get(i).getFechaIngreso(), med.get(i).getFechaCaducidad()};
            modelo.addRow(fila);
        }

    }

    public void borrartabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int fila = model.getRowCount();
        for (int i = 0; i < fila; i++) {
            model.removeRow(0);
        }

    }

