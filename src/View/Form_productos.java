    private void ReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReadActionPerformed
        // TODO add your handling code here:
        int ide = Integer.parseInt(txt_code.getText());
        probus = cp.findProductos(ide);
        if (probus == null) {
            JOptionPane.showMessageDialog(this, "Code " + ide + " non-existent ");
        } else {

            txt_nom.setText(probus.getNombre());
            jComboBox_cat.setSelectedItem(probus.getCategoriasCodigo());
            jComboBox_pro.setSelectedItem(probus.getProveedoresCodigo());
            txt_des.setText(probus.getDescripcion());
           
            probus.getFechaIngreso();
            jDateChooser.setDate(probus.getFechaCaducidad());
            mconsulta(probus);

        }
    }//GEN-LAST:event_ReadActionPerformed

    private void CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateActionPerformed
        // TODO add your handling code here:

        if (ValidarCampo()) {
            JOptionPane.showMessageDialog(this, "empty field");
        } else {
            try {
                int codigo = Integer.parseInt(txt_code.getText());
                String nombre = txt_nom.getText();
                String descrip = txt_des.getText();
                int unida = Integer.parseInt(txt_unid.getText());
                int precio = Integer.parseInt(txt_precio.getText());
                int cant_ing = Integer.parseInt(txt_cant_ing.getText());
                int cant_dis = Integer.parseInt(txt_cant_ing.getText());
                Date fecha = jDateChooser.getDate();
                java.sql.Date fechas = new java.sql.Date(fecha.getTime());
                LocalDate fechaloc = LocalDate.now();
                Date fechaact = Date.from(fechaloc.atStartOfDay(ZoneId.systemDefault()).toInstant());
                java.sql.Date fechaingre = new java.sql.Date(fechaact.getTime());
                pr.setCodigo(codigo);
                pr.setNombre(nombre);
                cat = (Categorias) jComboBox_cat.getSelectedItem();
                pr.setCategoriasCodigo(cat);
                p = (Proveedores) jComboBox_pro.getSelectedItem();
                pr.setProveedoresCodigo(p);
                pr.setDescripcion(descrip);
                pr.setUnidadMedida(unida);
                pr.setPrecioUnitario(precio);
                pr.setCantidadIngreso(cant_ing);
                pr.setCantidadDisponible(cant_dis);
                pr.setFechaIngreso(fechaingre);
                pr.setFechaCaducidad(fechas);
                
                cp.create(pr);
                agregarregistro(codigo, nombre, cat.getNombre(), p.getNombre(), descrip, unida, precio, cant_ing, cant_dis, fechaingre, fechas);
                JOptionPane.showMessageDialog(this, "Record saved successfully");
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(Form_productos.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            

        }
    }//GEN-LAST:event_CreateActionPerformed

