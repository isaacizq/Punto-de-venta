    public void cargartabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<Usuario> si = cu.findUsuarioEntities();
        for (int i = 0; i < si.size(); i++) {
            Object fila[] = {si.get(i).getIdusuario(), si.get(i).getContrasena(), si.get(i).getNombreCompleto()};
            model.addRow(fila);
        }
        
    }
    
    public boolean ValidarCampo() {
        if (txt_code.getText().equals("")) {
            txt_code.requestFocus();
            return true;
        }
        if (txt_pass.getText().equals("")) {
            txt_pass.requestFocus();
            return true;
        }
        return false;
    }
    
    public void agregarregistro(int codigo, String nombre, String password) {
        Object fila[] = {codigo, nombre, password};
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(fila);
    }
    
    public void borrartabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int fila = model.getRowCount();
        for (int i = 0; i < fila; i++) {
            model.removeRow(0);
        }
        
    }
    
    public void mconsulta(Usuario pre) {
        Object fila[] = {
            pre.getIdusuario(),
            pre.getContrasena(),
            pre.getNombreCompleto()
        };
        DefaultTableModel mod = (DefaultTableModel) jTable1.getModel();
        mod.setRowCount(0);
        mod.addRow(fila);
    }

