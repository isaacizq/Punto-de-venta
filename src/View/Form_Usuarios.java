    private void ReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReadActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txt_code.getText());
        obj = cu.findUsuario(id);
        if (obj == null) {
            JOptionPane.showMessageDialog(this, "Code " + id + " non-existent ");
        } else {
            txt_nom.setText(obj.getNombreCompleto());
            txt_pass.setText(obj.getContrasena());
            mconsulta(obj);
        }

    }//GEN-LAST:event_ReadActionPerformed

    private void CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateActionPerformed
        // TODO add your handling code here:
        if (ValidarCampo()) {
            JOptionPane.showMessageDialog(this, "Empty Field");
        } else {
            int id = Integer.parseInt(txt_code.getText());
            String pass = txt_pass.getText();
            String name = txt_nom.getText();
            u.setIdusuario(id);
            u.setContrasena(pass);
            u.setNombreCompleto(name);
            try {
                cu.create(u);
                JOptionPane.showMessageDialog(this, "Record saved successfully");
                agregarregistro(id, pass, name);
                txt_pass.setText("");
                txt_code.setText("");
                txt_nom.setText("");
            } catch (Exception ex) {
                Logger.getLogger(Form_Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_CreateActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        String pass = txt_pass.getText();
        String name = txt_nom.getText();
        obj.setContrasena(pass);
        obj.setNombreCompleto(name);
        try {
            cu.edit(obj);
            JOptionPane.showMessageDialog(this, "Record Edit successfully");
            borrartabla();
            cargartabla();
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(Form_Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(Form_Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        

    }//GEN-LAST:event_UpdateActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int rta = JOptionPane.showConfirmDialog(this, "Are you sure to delete this record?");
        if (rta == 0) {
            int id = Integer.parseInt(txt_code.getText());
            try {
                cu.destroy(id);
                JOptionPane.showMessageDialog(this, "Record Delete successfully");
                borrartabla();
                cargartabla();
                txt_code.setText("");
                txt_pass.setText("");
                txt_nom.setText("");
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(Form_Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_DeleteActionPerformed
    }
